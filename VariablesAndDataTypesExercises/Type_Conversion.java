package VariablesAndDataTypesExercises;
public class Type_Conversion {
    public static void main(String[] args) {
        // Convert int to double
        int intValue = 10;
        double doubleValue = (double) intValue;

        // Convert double to int
        double anotherDoubleValue = 15.75;
        int anotherIntValue = (int) anotherDoubleValue;

        // Print the results
        System.out.println("Converted int to double: " + doubleValue);
        System.out.println("Converted double to int: " + anotherIntValue);
    }
}


